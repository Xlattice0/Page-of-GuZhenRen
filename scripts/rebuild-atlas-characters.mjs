function parseGuName(raw) {
  // "春秋蝉(六转)" → { name: "春秋蝉", rank: "六转" }
  const match = raw.match(/^(.+?)\((.+?)\)$/);
  if (match) return { name: match[1].trim(), rank: match[2].trim() };
  return { name: raw.trim(), rank: "?转" };
}

function buildImmortalGuProfile(guList) {
  if (!guList || !guList.length) return null;
  const groups = new Map(); // path → items[]
  for (const raw of guList) {
    const { name, rank } = parseGuName(raw);
    const path = GU_PATH[name] || GU_PATH[raw] || "待考";
    if (!groups.has(path)) groups.set(path, []);
    const item = { name };
    if (rank) item.rank = rank;
    groups.get(path).push(item);
  }
  const result = [];
  for (const [path, items] of groups) {
    result.push({ path, items });
  }
  return result.length ? { groups: result } : null;
}

